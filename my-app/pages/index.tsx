const contractAddress = '0xd0a07b123c25f5e40491fdab714921c55701969d';

const Home: NextPage = () => {
  const [toAddress, setToAddress] = useState('');
  const [tokenId, setTokenId] = useState(-1);
  const [burnTokenId, setBurnTokenId] = useState(-1);

  const connectWallet = async () => {
    const { ethereum } = window;
    try {
      await ethereum.request({
        method: 'eth_requestAccounts',
      });
    } catch (err) {}
  };

  const mint = async () => {
    const { ethereum } = window;
    const provider = new ethers.providers.Web3Provider(ethereum);
    const signer = provider.getSigner();
    const signerAddress = signer.getAddress();
    const nftContract = new ethers.Contract(contractAddress, abi, provider);
    const tx = await nftContract.connect(signer).mint(signerAddress);

    console.log('mint button');
  };

  const handleTransfer = async (e: React.MouseEvent<HTMLInputElement>) => {
    e.preventDefault();
    const { ethereum } = window;
    const provider = new ethers.providers.Web3Provider(ethereum);
    const signer = provider.getSigner();
    const signerAddress = await signer.getAddress();
    const nftContract = new ethers.Contract(contractAddress, abi, provider);

    const tx = await nftContract
      .connect(signer)
      ['safeTransferFrom(address,address,uint256)'](
        signerAddress,
        toAddress,
        tokenId
      );
    console.log(`Transfer nft #${tokenId} to ${toAddress}`);
    setToAddress('');
    setTokenId(-1);
  };

  const handleBurn = async (e: React.MouseEvent<HTMLInputElement>) => {
    e.preventDefault();
    const { ethereum } = window;
    const provider = new ethers.providers.Web3Provider(ethereum);
    const signer = provider.getSigner();
    const signerAddress = await signer.getAddress();
    const nftContract = new ethers.Contract(contractAddress, abi, provider);

    const tx = await nftContract.connect(signer)['burn(uint256)'](burnTokenId);
    setBurnTokenId(-1);
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h2>Contract address : {contractAddress}</h2>
        <h2>Connect your wallet!ðŸ¦’</h2>
        <button onClick={connectWallet}>Connect wallet</button>
        <br />
        <h2>Mint an NFT!ðŸ¦’</h2>
        <button onClick={mint}>mint button</button>
        <br />
        <h2>Tranfer NFT!ðŸ¦’</h2>
        <form onSubmit={(e) => handleTransfer(e)}>
          <input
            type="text"
            placeholder="toAddress"
            onChange={(e) => setToAddress(e.target.value)}
            value={toAddress}
          />
          <input
            type="number"
            placeholder="tokenId"
            onChange={(e) => setTokenId(e.target.value)}
            value={tokenId}
          />
          <button>transfer NFT to{}</button>
        </form>
        <h2>Burn Your NFT!ðŸ¦’</h2>
        <form onSubmit={(e) => handleBurn(e)}>
          <input
            type="number"
            placeholder="burnTokenId"
            onChange={(e) => setBurnTokenId(e.target.value)}
            value={burnTokenId}
          />
          <button>Burn!{}</button>
        </form>
      </main>
    </div>
  );
};

export default Home;
